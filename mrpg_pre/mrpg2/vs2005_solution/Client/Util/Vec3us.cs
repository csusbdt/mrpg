using System;
using System.Collections.Generic;
using System.Text;

namespace Client
{
    struct Vec3us
    {
        public UInt16 x;
        public UInt16 y;
        public UInt16 z;

        public Vec3us(UInt16 x, UInt16 y, UInt16 z)
        {
            this.x = x;
            this.y = y;
            this.z = z;
        }
    }
}
